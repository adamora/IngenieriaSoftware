#ifndef REDSOCIAL_H_
#define REDSOCIAL_H_

#include<string>

using namespace std;

namespace Dentista{

	class RedSocial {
		private:
			string _nombreRed;
			string _usuario;
		public:
			inline RedSocial()
			{
				_nombreRed="NULL";
				_usuario="NULL";
			}
			//virtual ~RedSocial();

			inline void setNombreRed(const string nombreRed)
			{
				_nombreRed=nombreRed;
			}

			inline string getNombreRed() const
			{
				return _nombreRed;
			}

			inline void setUsuario(const string usuario)
			{
				_usuario=usuario;
			}

			inline string getUsuario() const
			{
				return _usuario;
			}
	};

}
#endif /* REDSOCIAL_H_ */
